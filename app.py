def build_recall_test(batch, include_identify):
    return {
        "type": "recall",
        "include_identify": include_identify,
        "card_ids": [c["id"] for c in batch],
    }


def build_quiz(batch, rng):
    """Generate a mixed set of aggregate + knowledge questions from the batch."""
    qs = []
    serials = [c["serial"] for c in batch]

    # --- aggregate: count a digit across all serials ---
    digit = rng.choice("0123456789")
    cnt = sum(s.count(digit) for s in serials)
    qs.append({"kind": "num",
               "prompt": f"Wie oft kommt die Ziffer **{digit}** in allen "
                         f"Ausweisnummern dieses Batches zusammen vor?",
               "answer": cnt})

    # --- aggregate: how many take medication ---
    qs.append({"kind": "num",
               "prompt": "Wie viele Personen nehmen Medikamente (ja)?",
               "answer": sum(1 for c in batch if c["takes_medication"])})

    # --- aggregate: count a blood group ---
    bg = rng.choice(BLOOD)
    qs.append({"kind": "num",
               "prompt": f"Wie viele Personen haben Blutgruppe **{bg}**?",
               "answer": sum(1 for c in batch if c["blood_group"] == bg)})

    # --- aggregate: count a month ---
    month = rng.choice([c["birth_month"] for c in batch])
    qs.append({"kind": "num",
               "prompt": f"Wie viele Personen haben im **{month}** Geburtstag?",
               "answer": sum(1 for c in batch if c["birth_month"] == month)})

    # --- knowledge: was this name in the batch? (true / decoy) ---
    if rng.random() < 0.5:
        real = rng.choice(batch)
        qs.append({"kind": "choice", "options": ["ja", "nein"],
                   "prompt": f"War der Name **{real['name']}** in diesem Batch?",
                   "answer": "ja"})
    else:
        names = {c["name"] for c in batch}
        fake = rng.choice(batch)["name"]
        # mutate a letter so it's not in batch
        while fake in names:
            i = rng.randrange(len(fake))
            fake = fake[:i] + rng.choice("ABCDEFGHIJKLMNOPQRSTUVWXYZ") + fake[i + 1:]
        qs.append({"kind": "choice", "options": ["ja", "nein"],
                   "prompt": f"War der Name **{fake}** in diesem Batch?",
                   "answer": "nein"})

    # --- knowledge: which allergy did <name> have? ---
    card = rng.choice(batch)
    correct = rng.choice(card["allergies"])
    distractors = [a for a in ALLERGY_POOL if a not in card["allergies"]]
    opts = rng.sample(distractors, 3) + [correct]
    rng.shuffle(opts)
    qs.append({"kind": "choice", "options": opts,
               "prompt": f"Welche dieser Allergien hatte **{card['name']}**?",
               "answer": correct})

    # --- knowledge: which country issued <name>'s card? ---
    card = rng.choice(batch)
    countries = list({c["country"] for c in CARDS})
    distractors = [c for c in countries if c != card["country"]]
    opts = rng.sample(distractors, min(3, len(distractors))) + [card["country"]]
    rng.shuffle(opts)
    qs.append({"kind": "choice", "options": opts,
               "prompt": f"In welchem Land wurde der Ausweis von "
                         f"**{card['name']}** ausgestellt?",
               "answer": card["country"]})

    # --- knowledge with image: who is this (match face -> name)? ---
    if len(batch) >= 2:
        card = rng.choice(batch)
        others = [c for c in batch if c["id"] != card["id"]]
        opts_cards = rng.sample(others, min(3, len(others))) + [card]
        rng.shuffle(opts_cards)
        qs.append({"kind": "face",
                   "prompt": "Wie heißt die abgebildete Person?",
                   "face_id": card["id"],
                   "options": [c["name"] for c in opts_cards],
                   "answer": card["name"]})

    rng.shuffle(qs)
    return {"type": "quiz", "questions": qs, "card_ids": [c["id"] for c in batch]}


