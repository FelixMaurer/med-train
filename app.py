@st.cache_data(show_spinner=False)
def load_cards():
    if not DB_PATH.exists():
        return []
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    rows = conn.execute("SELECT * FROM cards ORDER BY id").fetchall()
    conn.close()
    cards = []
    for r in rows:
        d = dict(r)
        d["allergies"] = json.loads(d["allergies"])
        d["takes_medication"] = bool(d["takes_medication"])
        cards.append(d)
    return cards


@st.cache_data(show_spinner=False)
def load_faces():
    """Recursively glob faces/, return a deterministic sorted list of paths."""
    if not FACES_DIR.exists():
        return []
    exts = ("*.png", "*.PNG", "*.jpg", "*.jpeg", "*.JPG", "*.JPEG")
    files = []
    for ext in exts:
        files.extend(FACES_DIR.rglob(ext))
    return sorted(str(f) for f in files)


@st.cache_data(show_spinner=False)
def placeholder_face(seed: int):
    """Gray placeholder used when no faces folder is present."""
    img = Image.new("RGB", (180, 200), (220, 220, 224))
    d = ImageDraw.Draw(img)
    d.ellipse((60, 40, 120, 100), fill=(180, 180, 186))      # head
    d.ellipse((45, 110, 135, 210), fill=(180, 180, 186))     # shoulders
    d.text((58, 175), "Foto", fill=(120, 120, 126))
    return img


def face_for(card, faces):
    if faces:
        return faces[card["face_index"] % len(faces)]
    return placeholder_face(card["face_index"])


CARDS = load_cards()
FACES = load_faces()

# --------------------------------------------------------------------------- #
#  Session state
# --------------------------------------------------------------------------- #
def init_state():
    ss = st.session_state
    ss.setdefault("phase", "home")          # home | study | recall | quiz | results
    ss.setdefault("batch_ids", [])          # ids of current batch
    ss.setdefault("batch_size", 4)
    ss.setdefault("history", [])            # list of result dicts
    ss.setdefault("test", None)             # built test payload
    ss.setdefault("last_result", None)
    # try loading local progress once
    ss.setdefault("_loaded_local", False)
    if not ss["_loaded_local"]:
        ss["_loaded_local"] = True
        if PROGRESS_FILE.exists():
            try:
                data = json.loads(PROGRESS_FILE.read_text())
                ss["history"] = data.get("history", [])
                ss["batch_ids"] = data.get("batch_ids", [])
                ss["batch_size"] = data.get("batch_size", 4)
            except Exception:
                pass


init_state()


def current_batch():
    by_id = {c["id"]: c for c in CARDS}
    return [by_id[i] for i in st.session_state["batch_ids"] if i in by_id]


def save_payload():
    return {
        "history": st.session_state["history"],
        "batch_ids": st.session_state["batch_ids"],
        "batch_size": st.session_state["batch_size"],
        "saved_at": datetime.now().isoformat(timespec="seconds"),
    }


def autosave_local():
    try:
        PROGRESS_FILE.write_text(json.dumps(save_payload(), ensure_ascii=False, indent=2))
    except Exception:
        pass


