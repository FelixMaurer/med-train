def view_quiz():
    ss = st.session_state
    test = ss["test"]
    by_id = {c["id"]: c for c in CARDS}
    st.title("Wissensquiz")
    st.caption("Fragen zum gerade gelernten Batch.")

    answers = []
    for i, q in enumerate(test["questions"]):
        with st.container(border=True):
            st.markdown(f"**Frage {i+1}.** {q['prompt']}")
            if q["kind"] == "num":
                val = st.number_input("Antwort", min_value=0, step=1,
                                      key=f"q{i}", value=0)
                answers.append(("num", val))
            elif q["kind"] == "choice":
                val = st.radio("Antwort", q["options"], key=f"q{i}", index=None,
                               horizontal=len(q["options"]) <= 3)
                answers.append(("choice", val))
            elif q["kind"] == "face":
                card = by_id[q["face_id"]]
                cc = st.columns([1, 3])
                with cc[0]:
                    st.image(face_for(card, FACES), width='stretch')
                with cc[1]:
                    val = st.radio("Name", q["options"], key=f"q{i}", index=None)
                answers.append(("choice", val))

    if st.button("✅ Auswerten", type="primary", width='stretch'):
        grade_quiz(test, answers)
        st.rerun()


def grade_quiz(test, answers):
    details = []
    score = 0
    for q, (_, given) in zip(test["questions"], answers):
        if q["kind"] == "num":
            ok = int(given) == int(q["answer"])
        else:
            ok = given == q["answer"]
        score += 1 if ok else 0
        details.append({"prompt": q["prompt"],
                        "your": "—" if given is None else given,
                        "correct": q["answer"], "ok": ok})
    finalize_result("Wissensquiz", score, len(test["questions"]),
                    details, is_quiz=True)


# --------------------------------------------------------------------------- #
#  Results
# --------------------------------------------------------------------------- #
def finalize_result(mode, score, max_pts, details, is_quiz=False):
    ss = st.session_state
    pct = round(100 * score / max_pts) if max_pts else 0
    ss["history"].append({
        "ts": datetime.now().strftime("%Y-%m-%d %H:%M"),
        "mode": mode, "size": len(ss["batch_ids"]),
        "score": score, "max": max_pts, "pct": pct,
    })
    ss["last_result"] = {"mode": mode, "score": score, "max": max_pts,
                         "pct": pct, "details": details, "is_quiz": is_quiz}
    ss["phase"] = "results"
    autosave_local()


def view_results():
    ss = st.session_state
    r = ss["last_result"]
    st.title("Ergebnis")
    c1, c2, c3 = st.columns(3)
    c1.metric("Modus", r["mode"])
    c2.metric("Punkte", f'{r["score"]} / {r["max"]}')
    c3.metric("Score", f'{r["pct"]}%')
    st.progress(r["pct"] / 100)

    st.divider()
    if r["is_quiz"]:
        for i, d in enumerate(r["details"], 1):
            icon = "✅" if d["ok"] else "❌"
            st.markdown(f"{icon} **{i}.** {d['prompt']}")
            if not d["ok"]:
                st.caption(f"Deine Antwort: {d['your']} · Richtig: **{d['correct']}**")
    else:
        for d in r["details"]:
            card = d["card"]
