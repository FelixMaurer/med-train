def view_recall():
    ss = st.session_state
    test = ss["test"]
    batch = current_batch()
    include_id = test.get("include_identify", False)
    st.title("Lückentest")
    st.caption("Gesicht (und Name) sind vorgegeben. Reproduziere die übrigen Felder.")
    include_id = st.checkbox("Auch Name & Allergien abfragen (schwer)",
                             value=include_id)
    test["include_identify"] = include_id

    answers = {}
    for idx, card in enumerate(batch):
        with st.container(border=True):
            cc = st.columns([1, 3])
            with cc[0]:
                st.image(face_for(card, FACES), width='stretch')
            with cc[1]:
                if include_id:
                    name = st.text_input("Name", key=f"name_{idx}")
                else:
                    st.markdown(f"**Name:** {card['name']}")
                    name = None
                a, b = st.columns(2)
                day = a.text_input("Geburtstag (Tag)", key=f"day_{idx}")
                month = b.selectbox("Monat", ["—"] + MONTHS, key=f"month_{idx}")
                a, b = st.columns(2)
                meds = a.selectbox("Medikamente", ["—", "ja", "nein"], key=f"meds_{idx}")
                blood = b.selectbox("Blutgruppe", ["—"] + BLOOD, key=f"blood_{idx}")
                a, b = st.columns(2)
                serial = a.text_input("Ausweisnummer", key=f"serial_{idx}")
                country = b.text_input("Ausstellungsland", key=f"country_{idx}")
                if include_id:
                    allergies = st.text_input(
                        "Allergien (Komma-getrennt)", key=f"all_{idx}")
                else:
                    allergies = None
        answers[card["id"]] = dict(name=name, day=day, month=month, meds=meds,
                                   blood=blood, serial=serial, country=country,
                                   allergies=allergies)

    if st.button("✅ Auswerten", type="primary", width='stretch'):
        grade_recall(batch, answers, include_id)
        st.rerun()


def norm(s):
    return (s or "").strip().lower()


def grade_recall(batch, answers, include_id):
    details = []
    score = 0
    max_pts = 0
    for card in batch:
        a = answers[card["id"]]
        items = []

        # date (day + month together)
        date_ok = (norm(a["day"]) == str(card["birth_day"])
                   and a["month"] == card["birth_month"])
        items.append(("Geburtsdatum", f'{card["birth_day"]}. {card["birth_month"]}',
                      f'{a["day"] or "—"}. {a["month"]}', date_ok))

        meds_truth = "ja" if card["takes_medication"] else "nein"
        meds_ok = a["meds"] == meds_truth
        items.append(("Medikamente", meds_truth, a["meds"], meds_ok))

        blood_ok = a["blood"] == card["blood_group"]
        items.append(("Blutgruppe", card["blood_group"], a["blood"], blood_ok))

        serial_ok = norm(a["serial"]) == card["serial"]
        items.append(("Ausweisnummer", card["serial"], a["serial"] or "—", serial_ok))

        country_ok = norm(a["country"]) == norm(card["country"])
        items.append(("Ausstellungsland", card["country"], a["country"] or "—", country_ok))

        if include_id:
            name_ok = norm(a["name"]) == norm(card["name"])
            items.append(("Name", card["name"], a["name"] or "—", name_ok))
            given = {x.strip().lower() for x in (a["allergies"] or "").split(",") if x.strip()}
            truth = {x.lower() for x in card["allergies"]}
            inter = len(given & truth)
            all_ok = given == truth
            items.append((f"Allergien ({inter}/{len(truth)} richtig)",
                          ", ".join(card["allergies"]), a["allergies"] or "—", all_ok))

        for _, _, _, ok in items:
            max_pts += 1
            score += 1 if ok else 0
        details.append({"card": card, "items": items})

    finalize_result("Lückentest", score, max_pts, details)


# --------------------------------------------------------------------------- #
#  Phase: quiz
