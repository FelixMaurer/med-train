def render_card(card, faces, show_country=True, hide=None):
    """hide: set of field keys to blank out (for partially shown study/preview)."""
    hide = hide or set()
    st.markdown(CARD_CSS, unsafe_allow_html=True)
    with st.container(border=True):
        st.markdown(
            "<div class='aw-head'><span>Allergieausweis</span>"
            "<span class='aw-cross'>✚</span></div>", unsafe_allow_html=True)
        c1, c2 = st.columns([1, 2.4])
        with c1:
            st.image(face_for(card, faces), width='stretch')
        with c2:
            def line(label, value, key):
                val = "▢▢▢" if key in hide else value
                st.markdown(
                    f"<div class='aw-field'><span class='aw-label'>{label}:</span> "
                    f"<b>{val}</b></div>", unsafe_allow_html=True)
            line("Name", card["name"], "name")
            line("Geburtsdatum", f'{card["birth_day"]}. {card["birth_month"]}', "date")
            line("Medikamenteneinnahme", "ja" if card["takes_medication"] else "nein", "meds")
            line("Blutgruppe", card["blood_group"], "blood")
            line("Bekannte Allergien", ", ".join(card["allergies"]), "allergies")
            line("Ausweisnummer", card["serial"], "serial")
            if show_country:
                line("Ausstellungsland", card["country"], "country")


